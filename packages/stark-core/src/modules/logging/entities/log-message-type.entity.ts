/**
 * The type of logging messages that can be sent to the back-end
 */
export class StarkLogMessageType {
	/**
	 * The debug type
	 */
	public static DEBUG = "DEBUG";
	/**
	 * The info type
	 */
	public static INFO = "INFO";
	/**
	 * The warning type
	 */
	public static WARNING = "WARNING";
	/**
	 * The error type
	 */
	public static ERROR = "ERROR";
}
