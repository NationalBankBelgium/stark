/**
 * This class defines the different query parameters supported by Stark.
 */
export class StarkHttpQueryParameters {
	/**
	 * the fields concerned by the queries
	 */
	public static FIELDS = "fields";
	/**
	 * specifies the language of the content
	 */
	public static LANG = "lang";
	/**
	 * specifies the style to apply to the response's elements
	 */
	public static STYLE = "style";
	/**
	 * specifies if the response must be sorted or not
	 */
	public static SORT = "sort";
}
